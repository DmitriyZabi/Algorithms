const binarySearch = (input, target) => {
  let left = 0
  let right = input.length - 1
  let middle

  while (left <= right) {
    mid = Math.round(0.5 * (right - left) + left)
    if (input[mid] === target) {
      return mid
    }
    input[mid] > target ? (right = --mid) : (left = ++mid)
  }
  return -1
}

